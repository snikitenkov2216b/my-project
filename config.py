# ==================== ФИЗИКО-ХИМИЧЕСКИЕ КОНСТАНТЫ ====================
# Точные молярные массы (г/моль) согласно IUPAC

MOLAR_MASS_CO2 = 44.009  # CO2: C(12.011) + O2(15.999*2)
MOLAR_MASS_C = 12.011    # Углерод
