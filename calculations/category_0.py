# calculations/category_0.py - Модуль для расчетов по Категории 0.
# Инкапсулирует бизнес-логику для расчета расхода по балансовому методу.
# Комментарии на русском. Поддержка UTF-8.

class Category0Calculator:
    """
    Класс-калькулятор для категории 0: "Расчет расхода ресурсов по балансу".
    """

    @staticmethod
    def calculate_consumption(
         поступление: float, 
        отгрузка: float, 
        запас_начало: float, 
        запас_конец: float
    ) -> float:
        """
        Рассчитывает расход ресурса по данным о поступлении, отгрузке и изменении запасов.
        
        Реализует формулу 1 из методических указаний.
        M_расход = M_пост - M_отгр + M_запас_нач - M_запас_кон

        :param поступление: Масса поступившего ресурса за период.
        :param отгрузка: Масса отгруженного ресурса за период.
        :param запас_начало: Масса ресурса на складе в начале периода.
        :param запас_конец: Масса ресурса на складе в конце периода.
        :return: Рассчитанная масса расхода ресурса.
        """
        if any(val < 0 for val in [поступление, отгрузка, запас_начало, запас_конец]):
            raise ValueError("Значения массы не могут быть отрицательными.")
            
        расход = поступление - отгрузка + запас_начало - запас_конец
        
        return расход